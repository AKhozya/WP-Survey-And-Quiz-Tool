	<h3>Quizes</h3>
	
		<a name="create_a_quiz"></a>
		<h4>How to create a quiz?</h4>	
		<p>To create a quiz you need to go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then click <strong>`</strong><em>Add New Quiz</em>`</strong> button. Then complete the form and click the <strong>`<em>Save Quiz</em>`</strong> button.</p>
		
		<a name="edit_a_quiz"></a>
		<h4>How to edit a quiz?</h4>	
		<p>To edit a quiz you need to goto <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Edit</em>`</strong>.</p>
	
		<a name="delete_a_quiz"></a>
		<h4>How to delete a quiz?</h4>	
		<p>To delete a quiz you need to go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Delete</em>`</strong>.</p>
	
		<a name="how_many_quiz"></a>
		<h4>How many quizzes can I have?</h4>	
		<p>You can have as many as you would like. If you have issues create new quizzes please report this to us.</p>
	
		<a name="display_quiz"></a>
		<h4>How do I display a quiz on a page?</h4>
		<p>To display a quiz on a page you need to use the shortcode, [wpsqt_page name=""] in put in the name of the quiz you want to display in between the quote marks. You can do this in a post or in a page, a new seperate page is advised. <em>Note you may want to disable comments.</em></p>
	
	<h3>Questions</h3>
	
		<a name="create_a_question"></a>
		<h4>How do I create questions?</h4>	
		<p>To create a question <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz you wish to add questions too in the table list. Click <strong>`<em>Edit Questions</em>`</strong> then fill out the form. You will then see a list of questions to add a new question click <strong>`<em>Add New Question</em>`</strong> and fill out the form. Questions must be assigned to a section for them to be displayed!</p>
		
		<a name="edit_a_question"></a>
		<h4>How do I edit a question?</h4>	
		<p>To edit a quiz you need to go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Edit</em>`</strong> you will then see a list of questions locate the question you wish to edit and then click the <strong>`<em>Edit</em>`</strong> and fill out form accordingly.</p>
			
		<a name="delete_a_question"></a>
		<h4>How do I delete a question?</h4>	
		<p>To edit a quiz you need to go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz in the table list. When quiz is located click <strong>`<em>Edit</em>`</strong> you will then see a list of questions locate the question you wish to edit and then click the <strong>`<em>Delete</em>`</strong> and then confirm.</p>
		
		<a name="how_many_question"></a>
		<h4>How many question can I have?</h4>	
		<p>You can have as many as you would like. If you have issues create new questions please report this to us.</p>
		
		<a name="extra_html"></a>
		<h4>Is it possible to have html for the questions?</h4>
		<p>HTML is enabled for the <strong>`<em>additional</em>`</strong> field but not the actual question question.</p>
		
	<h3>Marking</h3>
		
		<a name="mark_result"></a>
		<h4>How do I mark a result?</h4>	
		<p>To mark a result, you go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz Results</em>`</strong> then click <strong>`<em>Mark</em>`</strong>. Then you can then see the answers for multiple choice questions with the correct one in Green text and any ones given that are incorrect in red. For questions as text area type the answers are given in the Grey boxed area with the marking choice below it aswell as an area for comments. There is also a hint area which be shown and hidden.</p>
		
		<a name="automarking"></a>
		<h4>What is auto marking?</h4>	
		<p>Auto marking is when the results are automatically marked upon completion. It can only be done on multiple choice only sections.</p>
		
		<a name="how_do_view_marked"></a>
		<h4>How do I view results that have already been marked?</h4>	
		<p>When a result is marked the <strong>`<em>Mark</em>`</strong> button disappears and is replaced with the score. The score is click-able and will lead you to the marking page where you can update the score and whatnot.</p>

	<h3>Sections</h3>

		<a name="what_are_sections"></a>
		<h4>What are sections?</h4>	
		<p>Sections are pages within the quiz. They each have a different name and the question type is configurable.</p>
		
		<a name="create_a_section"></a>
		<h4>How to create a section?</h4>	
		<p>To create a section you need to go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz for which the section belongs to then click <strong>`<em>Edit Sections </em>`</strong> button. Then complete the form and click the <strong>`<em>Save</em>`</strong> button.</p>
		
		<a name="edit_a_section"></a>
		<h4>How to edit a section?</h4>	
		<p>To create a section you need to go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz for which the section belongs to then click <strong>`<em>Edit Sections </em>`</strong> button. Then edit form and click the <strong>`<em>Save</em>`</strong> button.</p>
	
		<a name="delete_a_section"></a>
		<h4>How to delete a section?</h4>	
		<p>To delete a section you need to go to <strong>`<em>WP Survey And Quiz Tool</em>`</strong> -&gt; <strong>`<em>Quiz/Survey</em>`</strong> then locate the quiz for which the section belongs to then click <strong>`<em>Edit Sections </em>`</strong> button. Then empty the section's values and then click the <strong>`<em>Save</em>`</strong> button.</p>
	
		<a name="how_many_sections"></a>
		<h4>How many sections can I have?</h4>
		<p>You can have as many as you would like. If you have issues create new sections please report this to us.</p>
	
		<a name="number_of_questions"></a>
		<h4>A quiz section doesn't always show the correct number of questions whys this?</h4>
		<p>You will have set the difficulty to mixed. If you have set all your questions to the same difficulty please change the section difficulty to match. This is only to be used when there is an even number of questions for each difficulty and is advised to only be used when there are more questions than the question number.</p>
		
	<h3>Customization</h3>
	
		<a name="change_layout"></a>
		<h4>Can I change the quiz layout/style?</h4>
		<p>You can change the look of the quiz and surveys with ease by creating a new directory in the pages/custom directory. The custom directory naming schema is quiz-$quid_id for quizzes and survey-$survey_id. So for example if you wish to create custom layouts for a quiz with the id 3 it would be 'quiz-3'.</p>